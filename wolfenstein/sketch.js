let grid = []
let player;


function setup(){
  createCanvas(mapWidth + sceneWidth, mapHeight);
  frameRate(10);
  for (var y = 0; y < mapHeight / cellSize; y++) {
    grid[y] = [];
    for (var x = 0; x < mapWidth / cellSize; x++) {
      grid[y][x] = new cell(x * cellSize, y * cellSize, cellSize, tileMap[y][x]);
    }
  }

  player = new person(mapWidth / 2 , mapHeight / 2)

}

function draw() {
  background(0);
  fill(0, 255, 0)
  //rect(mapWidth, 0, sceneWidth, mapHeight)
  for (let ray of player.rays) {
    ray.show();
  }
  if (keyIsDown(87)) {
    player.setPos(player.direction.x / cellSize, player.direction.y / cellSize);
  }

  if (keyIsDown(83)) {
    player.setPos(-player.direction.x / cellSize, -player.direction.y / cellSize);
  }

  if (keyIsDown(65)) {
    player.setRotation(-0.08);
  }

  if (keyIsDown(68)) {
    player.setRotation(0.08);
  }

  for (var y = 0; y < grid.length; y++) {
    for (var x = 0; x < grid[y].length; x ++) {
      grid[y][x].show();
    }
  }

  player.show();
  player.render();

}
